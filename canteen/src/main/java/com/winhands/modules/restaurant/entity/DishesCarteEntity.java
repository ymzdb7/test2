package com.winhands.modules.restaurant.entity;


import java.io.Serializable;
/**
 * 食堂菜单菜品关联信息
 */
public class DishesCarteEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	
	/**
	 * ID
	 */
	private Long id;

	/**
	 *菜单id
	 */
	private Long carteId;

	/**
	 * 菜品iD
	 */
	private Long dishesId;

	/**
	 * 是否推荐   0：否  1：是
	 */
	private int isRecom;

	/**
	 * 推荐指数
	 */
	private String recomNum;

	/**
	 * 是否点赞  0 ：否   1：是
	 */
	private int isLike;

	/**
	 * 点赞数
	 */
	private String likeNum;
	
	/**
	 * 评分
	 */
	private String score;


	public Long getId() {
		return id;
	}


	public void setId(Long id) {
		this.id = id;
	}


	public Long getCarteId() {
		return carteId;
	}


	public void setCarteId(Long carteId) {
		this.carteId = carteId;
	}


	public Long getDishesId() {
		return dishesId;
	}


	public void setDishesId(Long dishesId) {
		this.dishesId = dishesId;
	}


	public int getIsRecom() {
		return isRecom;
	}


	public void setIsRecom(int isRecom) {
		this.isRecom = isRecom;
	}


	public String getRecomNum() {
		return recomNum;
	}


	public void setRecomNum(String recomNum) {
		this.recomNum = recomNum;
	}


	public int getIsLike() {
		return isLike;
	}


	public void setIsLike(int isLike) {
		this.isLike = isLike;
	}


	public String getLikeNum() {
		return likeNum;
	}


	public void setLikeNum(String likeNum) {
		this.likeNum = likeNum;
	}


	public String getScore() {
		return score;
	}


	public void setScore(String score) {
		this.score = score;
	}




	
}
