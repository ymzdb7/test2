package com.winhands.modules.restaurant.entity;


import java.io.Serializable;
/**
 * 菜品管理
 */
public class DishesEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	
	private Long id; // ID

	private String dishes;//菜名
	private String typeName;//种类

	private String dishesPicPath;//菜图片路径

	private String dishesPicName;//菜图片原名
	
	private String batching;//配料

	private String detailHtml;//html文章地址

	private String dishesDetail;//菜品详情
	
	private String keyword;//说明
	
	private Long createUserId;
	
	private int isRecom;//是否推荐  0：否  1：是
	
	private String recomNum;//推荐指数
	
	private String address;//属地

	
	
	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDishes() {
		return dishes;
	}

	public void setDishes(String dishes) {
		this.dishes = dishes;
	}

	public String getDishesPicPath() {
		return dishesPicPath;
	}

	public void setDishesPicPath(String dishesPicPath) {
		this.dishesPicPath = dishesPicPath;
	}

	public String getDishesPicName() {
		return dishesPicName;
	}

	public void setDishesPicName(String dishesPicName) {
		this.dishesPicName = dishesPicName;
	}

	public String getBatching() {
		return batching;
	}

	public void setBatching(String batching) {
		this.batching = batching;
	}

	public String getDishesDetail() {
		return dishesDetail;
	}

	public void setDishesDetail(String dishesDetail) {
		this.dishesDetail = dishesDetail;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public Long getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Long createUserId) {
		this.createUserId = createUserId;
	}

	public int getIsRecom() {
		return isRecom;
	}

	public void setIsRecom(int isRecom) {
		this.isRecom = isRecom;
	}

	public String getRecomNum() {
		return recomNum;
	}

	public void setRecomNum(String recomNum) {
		this.recomNum = recomNum;
	}

	public String getDetailHtml() {
		return detailHtml;
	}

	public void setDetailHtml(String detailHtml) {
		this.detailHtml = detailHtml;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	
	

	
}
