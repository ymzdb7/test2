package com.winhands.modules.restaurant.entity;


import java.io.Serializable;
import java.math.BigDecimal;
/**
 * 菜品管理
 */
public class OrderAdvanceDishesEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	private Long id; // ID
	private Long orderId;//订单id
	private Long advanceDishesId;
	private Integer count;
	private BigDecimal advDishesPrice;
	public BigDecimal getAdvDishesPrice() {
		return advDishesPrice;
	}
	public void setAdvDishesPrice(BigDecimal advDishesPrice) {
		this.advDishesPrice = advDishesPrice;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getOrderId() {
		return orderId;
	}
	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}
	public Long getAdvanceDishesId() {
		return advanceDishesId;
	}
	public void setAdvanceDishesId(Long advanceDishesId) {
		this.advanceDishesId = advanceDishesId;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}

}
